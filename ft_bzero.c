#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char *s1;

	s1 = (char *)s;
	if (n == 0)
		return ;
	else
		while (n != 0)
		{
			n--;
			*s1 = 0;
			s1++;
		}
}
