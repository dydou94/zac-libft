#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	int x;

	x = 0;
	while (src[x] != '\0' && n > 0)
	{
		dst[x] = src[x];
		x++;
		n--;
	}
	while (n != 0)
	{
		dst[x++] = '\0';
		n--;
	}
	return (dst);
}
